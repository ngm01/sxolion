package com.projects.sxolion.models;

public class Shelf {
	
	private String id;
	private String name;
	
	
	public Shelf() {
	}
	
	public Shelf(String name) {
		this.name = name;
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}
	
	

}
